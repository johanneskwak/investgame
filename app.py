def make_shares_slider(label: str, team, ticker: str, game, key: str) -> int:
    price = game.get_price(ticker, game.current_date)
    is_kr = ticker.endswith('.KS')
    balance = team.krw_balance if is_kr else team.usd_balance

    if price <= 0:
        return 1

    max_buy = int(balance / price)

    if max_buy < 1:
        return 1

    # step 안정화
    if max_buy >= 10000:
        step = 100
    elif max_buy >= 1000:
        step = 10
    elif max_buy >= 100:
        step = 5
    else:
        step = 1

    # value 안전 처리
    default_value = min(1, max_buy)

    return st.slider(
        label,
        min_value=1,
        max_value=max_buy,
        value=default_value,
        step=step,
        key=key
    )


